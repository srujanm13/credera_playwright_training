//Prints True
//== does loose equality check i.e. converts false value to 0 before comparing with 0 value
//0 value stands for false and 1 value stands for true
console.log(false == 0);

//Prints false
//=== Does strict equality check i.e. compares both the value and the datatype
//false value is of boolean data type and 0 value is of number data type
console.log(false === 0);